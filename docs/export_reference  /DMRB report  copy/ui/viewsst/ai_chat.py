# --------------------------------------------------
# Main render function (Streamlit UI only)
# --------------------------------------------------
def render(df: pd.DataFrame):

    st.title("🤖 Ops Assistant")
    st.caption("Ask anything about your turnover data — units, risks, SLAs, workload, priorities.")

    # --------------------------------------------------
    # Sidebar
    # --------------------------------------------------
    with st.sidebar:
        st.subheader("AI Settings")

        model = st.selectbox(
            "Model",
            ["gpt-4o", "gpt-4o-mini", "gpt-4.1", "gpt-4.1-mini", "gpt-4.1-nano"],
            key="chat_model",
        )

        # Memory stats
        total_msgs = get_total_conversation_count()
        insights = get_active_insights()
        st.caption(f"💾 {total_msgs} messages stored · {len(insights)} learned insights")

        if st.button("Clear Chat", key="chat_clear"):
            st.session_state["chat_messages"] = []
            st.rerun()

    if not OPENAI_API_KEY:
        st.warning("Open `ui/viewsst/ai_chat.py` and paste your OpenAI API key on line 20.")
        return

    # --------------------------------------------------
    # Prepare data
    # --------------------------------------------------
    df = enrich(df)
    df = prepare_data(df)

    # --------------------------------------------------
    # Build system prompt (with memory + trends + data)
    # --------------------------------------------------
    system_prompt = build_system_prompt(df, DOMAIN_PROMPT)

    # --------------------------------------------------
    # Session management
    # --------------------------------------------------
    if "chat_session_id" not in st.session_state:
        st.session_state["chat_session_id"] = new_session_id()

    if "chat_messages" not in st.session_state:
        st.session_state["chat_messages"] = []

    session_id = st.session_state["chat_session_id"]

    # Display existing messages
    for msg in st.session_state["chat_messages"]:
        with st.chat_message(msg["role"]):
            st.markdown(msg["content"])

    # --------------------------------------------------
    # User input
    # --------------------------------------------------
    user_input = st.chat_input("Ask about your data...")
    if not user_input:
        return

    st.session_state["chat_messages"].append({"role": "user", "content": user_input})
    with st.chat_message("user"):
        st.markdown(user_input)

    # --------------------------------------------------
    # Get response (engine handles API + memory + learning)
    # --------------------------------------------------
    with st.chat_message("assistant"):
        with st.spinner("Thinking..."):
            try:
                reply = chat(
                    user_input=user_input,
                    messages=st.session_state["chat_messages"],
                    api_key=OPENAI_API_KEY,
                    model=model,
                    system_prompt=system_prompt,
                    session_id=session_id,
                )
            except Exception as e:
                reply = f"⚠️ Error: {e}"
        st.markdown(reply)

    st.session_state["chat_messages"].append({"role": "assistant", "content": reply})
